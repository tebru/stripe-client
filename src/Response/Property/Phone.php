namespace Tebru\Stripe\Response\Property;

use JMS\Serializer\Annotation\Type;

/**
 * Trait Phone
 *
 * @author Nate Brunette <n@tebru.net>
 */
trait Phone
{
    /**
     * @var string
     *
     * @Type("string")
     */
    private $phone;

    /**
     * @return string
     */
    public function getPhone()
    {
        return $this->phone;
    }

    /**
     * @param string $phone
     * @return $this
     */
    public function setPhone($phone)
    {
        $this->phone = $phone;

        return $this;
    }
}
