namespace Tebru\Stripe\Model\Property;

use JMS\Serializer\Annotation\Groups;
use JMS\Serializer\Annotation\Type;

/**
 * Trait Reason
 *
 * @author Nate Brunette <n@tebru.net>
 */
trait Reason
{
    /**
     * @var string
     *
     * @Type("string")
     * @Groups({"RefundCreate"})
     */
    private $reason;

    /**
     * @return string
     */
    public function getReason()
    {
        return $this->reason;
    }

    /**
     * @param string $reason
     * @return $this
     */
    public function setReason($reason)
    {
        $this->reason = $reason;

        return $this;
    }
}
